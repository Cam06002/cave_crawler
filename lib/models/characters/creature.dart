class Creature {
  String description;
  int height;
  int width;
  int weight;
  int temperment; // 0-10 0 being docile, 10 being rabid
  int attack;
  int durability;
  int healthPoints;
  int speed;
  int sneak;
  int nutritionalValue;
  int monitaryValue;

  Creature(
      this.description,
      this.height,
      this.width,
      this.weight,
      this.temperment,
      this.attack,
      this.durability,
      this.healthPoints,
      this.speed,
      this.sneak,
      this.nutritionalValue,
      this.monitaryValue);
}
